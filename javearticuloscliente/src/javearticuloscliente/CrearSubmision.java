    public CrearSubmision(int usu) {
        //articulos = new JComboBox<String>();
        serv = new ApiSubmisionService();
        api = serv.getApiSubmisionPort();
        this.usu=usu;
        initComponents();
        
        
        List<Articulo> listArt = api.articuloUsuario(new BigDecimal(usu));
        for(Articulo a: listArt){
            articulos.addItem(String.valueOf(a.getIdarticulo()).toString());
        }
        
        List<Evento> listEv = api.eventos();
        for(Evento e: listEv){
            eventos.addItem(String.valueOf(e.getIdevento()).toString());
        }
        
    }

